/* eslint-disable @typescript-eslint/no-require-imports */
const { spawnSync } = require("node:child_process");
const { createHash } = require("node:crypto");
const {
  existsSync,
  mkdirSync,
  readFileSync,
  readdirSync,
  unlinkSync,
  writeFileSync,
} = require("node:fs");
const { join, resolve } = require("node:path");

const ROOT = resolve(__dirname, "..");
const SOURCE = join(ROOT, "audio");
const OUTPUT = join(ROOT, "public", "audio");
const MANIFEST = join(ROOT, "src", "lib", "audio-assets.ts");
const AMBIENT_TARGET_LUFS = -36;
const STEP_TARGET_LUFS = -27;
const NULL_DEVICE = process.platform === "win32" ? "NUL" : "/dev/null";

function requireCommand(command) {
  const result = spawnSync(command, ["-version"], { encoding: "utf8" });
  if (result.error || result.status !== 0) {
    throw new Error(`${command} is required to prepare audio assets`);
  }
}

function audioFiles(folder) {
  const path = join(SOURCE, folder);
  if (!existsSync(path)) throw new Error(`Missing source folder: ${path}`);
  return readdirSync(path)
    .filter((name) => /\.(wav|wave|aif|aiff|flac|mp3|m4a)$/i.test(name))
    .map((name) => ({ name, path: join(path, name) }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

function hashFile(path) {
  return createHash("sha256").update(readFileSync(path)).digest("hex");
}

function uniqueFiles(files) {
  const byHash = new Map();
  for (const file of files) {
    const hash = hashFile(file.path);
    const current = byHash.get(hash);
    const isCopyName = / \(\d+\)(?=\.[^.]+$)/.test(file.name);
    const currentIsCopy = current
      ? / \(\d+\)(?=\.[^.]+$)/.test(current.name)
      : true;
    if (!current || (currentIsCopy && !isCopyName)) byHash.set(hash, file);
  }
  return [...byHash.values()].sort((a, b) => a.name.localeCompare(b.name));
}

function run(command, args, label) {
  const result = spawnSync(command, args, {
    cwd: ROOT,
    encoding: "utf8",
    maxBuffer: 20 * 1024 * 1024,
  });
  if (result.error || result.status !== 0) {
    const detail = result.stderr?.trim() || result.error?.message || "unknown error";
    throw new Error(`${label} failed:\n${detail}`);
  }
  return result;
}

function analyzeLoudness(input, target) {
  const result = run(
    "ffmpeg",
    [
      "-hide_banner",
      "-nostats",
      "-i",
      input,
      "-map",
      "0:a:0",
      "-af",
      `loudnorm=I=${target}:TP=-2:LRA=20:print_format=json`,
      "-f",
      "null",
      NULL_DEVICE,
    ],
    `Loudness analysis for ${input}`,
  );
  const match = result.stderr.match(/\{[\s\S]*?"target_offset"\s*:\s*"[^"]+"\s*\}/g);
  if (!match?.length) throw new Error(`Could not parse loudness analysis for ${input}`);
  return JSON.parse(match.at(-1));
}

function loudnessFilter(input, target) {
  const measured = analyzeLoudness(input, target);
  return [
    `loudnorm=I=${target}`,
    "TP=-2",
    "LRA=20",
    `measured_I=${measured.input_i}`,
    `measured_TP=${measured.input_tp}`,
    `measured_LRA=${measured.input_lra}`,
    `measured_thresh=${measured.input_thresh}`,
    `offset=${measured.target_offset}`,
    "linear=true",
    "print_format=summary",
  ].join(":");
}

function resetOutputFolder(folder) {
  mkdirSync(folder, { recursive: true });
  for (const name of readdirSync(folder)) {
    const path = join(folder, name);
    if (/\.(webm|wav)$/i.test(name)) unlinkSync(path);
  }
}

function convertAmbient(file, output) {
  const filter = loudnessFilter(file.path, AMBIENT_TARGET_LUFS);
  run(
    "ffmpeg",
    [
      "-y",
      "-hide_banner",
      "-i",
      file.path,
      "-map",
      "0:a:0",
      "-vn",
      "-map_metadata",
      "-1",
      "-af",
      `${filter},aresample=48000`,
      "-ac",
      "2",
      "-c:a",
      "libopus",
      "-b:a",
      "160k",
      "-application",
      "audio",
      output,
    ],
    `Ambient conversion for ${file.name}`,
  );
}

function convertStep(file, output) {
  const filter = loudnessFilter(file.path, STEP_TARGET_LUFS);
  run(
    "ffmpeg",
    [
      "-y",
      "-hide_banner",
      "-i",
      file.path,
      "-map",
      "0:a:0",
      "-vn",
      "-map_metadata",
      "-1",
      "-af",
      `${filter},aresample=48000`,
      "-ar",
      "48000",
      "-ac",
      "2",
      "-c:a",
      "pcm_s16le",
      output,
    ],
    `Footstep conversion for ${file.name}`,
  );
}

function formatManifest(ambientUrls, footstepUrls) {
  const format = (name, urls) =>
    `export const ${name} = [\n${urls.map((url) => `  "${url}",`).join("\n")}\n] as const;`;
  return [
    "/** Generated by `npm run audio:prepare`. Do not edit by hand. */",
    format("AMBIENT_AUDIO_URLS", ambientUrls),
    "",
    format("FOOTSTEP_AUDIO_URLS", footstepUrls),
    "",
  ].join("\n");
}

function main() {
  requireCommand("ffmpeg");
  requireCommand("ffprobe");

  const ambient = uniqueFiles(audioFiles("ambient"));
  const steps = uniqueFiles(audioFiles("steps"));
  const ambientOutput = join(OUTPUT, "ambient");
  const stepsOutput = join(OUTPUT, "steps");
  resetOutputFolder(ambientOutput);
  resetOutputFolder(stepsOutput);

  const ambientUrls = ambient.map((file, index) => {
    const name = `ambient-${String(index + 1).padStart(2, "0")}.webm`;
    console.log(`ambient: ${file.name} -> ${name}`);
    convertAmbient(file, join(ambientOutput, name));
    return `/audio/ambient/${name}`;
  });
  const footstepUrls = steps.map((file, index) => {
    const name = `step-${String(index + 1).padStart(2, "0")}.wav`;
    console.log(`step: ${file.name} -> ${name}`);
    convertStep(file, join(stepsOutput, name));
    return `/audio/steps/${name}`;
  });

  writeFileSync(MANIFEST, formatManifest(ambientUrls, footstepUrls), "utf8");
  console.log(`Prepared ${ambientUrls.length} ambient and ${footstepUrls.length} step assets.`);
}

main();
